def get_dua_of_day() -> dict:
    from datetime import datetime, timezone
    day_of_year = datetime.now(timezone.utc).timetuple().tm_yday
    return DUAS[day_of_year % len(DUAS)]


def search_duas(query: str) -> list[dict]:
    q = query.lower().strip()
    all_duas = DUAS + MORNING_ADHKAR + EVENING_ADHKAR + AFTER_PRAYER_DUAS
    results = []
    seen_titles = set()
    for dua in all_duas:
        if dua["title"] in seen_titles:
            continue
        title_match = q in dua["title"].lower()
        translation_match = q in dua["translation"].lower()
        tags_match = any(q in tag.lower() for tag in dua.get("tags", []))
        if title_match or translation_match or tags_match:
            results.append(dua)
            seen_titles.add(dua["title"])
    return results


